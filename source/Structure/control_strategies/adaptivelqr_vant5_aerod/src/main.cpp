    double y_atual = Trajetoria(1) - msg.values.at(1);
    yint = yint + (T / 2.0) * (y_atual + y_ant);
    y_ant = y_atual;

    Eigen::VectorXd Xki(3);
    Xki << xint, yint, 0;

    // kinematic controller  action
    Eigen::VectorXd Dk(3);
    Dk = Kk * RIB.transpose() * Xk + Kki * RIB.transpose() * Xki + Uk;
    //	Dk = Kk*RIB.transpose()*Xk+Uk;
    Trajetoria(10) = Dk(0);
    Trajetoria(11) = Dk(1);
    Trajetoria(12) = Dk(2);

